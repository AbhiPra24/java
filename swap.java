public class swap{
public static void main(String args[]){
int x=5;
int y=1;
System.out.println("Before swapping");
System.out.println("First no." +x);
System.out.println("Second no." +y);
int temp=x;
x=y;
y=temp;

System.out.println("Before swapping");
System.out.println("First no." +x);
System.out.println("Second no." +y);
}}



















